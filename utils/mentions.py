# Internal shuffled pool
_emoji_pool = []


def _get_next_emoji():
    global _emoji_pool

    if not _emoji_pool:
        _emoji_pool = EMOJIS.copy()
        random.shuffle(_emoji_pool)

    return _emoji_pool.pop()


def make_mention(user):
    emoji = _get_next_emoji()
    return f"[{emoji}](tg://user?id={user.id})"
